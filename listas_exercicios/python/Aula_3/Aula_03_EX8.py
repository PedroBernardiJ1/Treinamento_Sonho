angulo_1 = float(input("Qual o valor do primeiro ângulo? "))
angulo_2 = float(input("Qual o valor do segundo ângulo? "))
angulo_3 = float(input("Qual o valor do terceiro ângulo? "))

if angulo_1 + angulo_2 + angulo_3 == 180:
    print ("Os ângulos formam um triângulo!")
else:
    print ("Os ângulos não formam um triângulo!")